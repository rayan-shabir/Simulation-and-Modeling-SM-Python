
#int CONVERSIONS
print(int(3.7))

print(int(True))

# print(int("a"))  #---> ERROR ... WE cannot convert string to integer

print(int("77"))  #---> But if string have only numbers then we can convert it into int

#float CONVERSIONS
print(float(7))

print(float(True))

print(float("77"))

print(float(2.1e-2))

print(float("2.1e-2"))

#string CONVERSIONS
print(str(7.7))

print(str(True))

print(str([1, 2, 3]))

#bool CONVERSIONS
print(bool(3))

print(bool(0))

print(bool(0.1))

print(bool([1, 2, 3]))

print(bool())

print(bool([]))

print(bool({}))

#sets CONVERSIONS
print(set([1, 2, 2, 3]))

#list CONVERSIONS
print(list("123"))

print(list({1, 2, 2, 3}))

print(list({'a':1, 'b':2}))

#tuple CONVERSIONS
print(tuple([1, 2, 3]))



